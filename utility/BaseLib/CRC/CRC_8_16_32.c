/**
  * @brief  Get Buff CRC8
  * @param  *Buff Input the needs caulate
  *         BuffStart Can use buff Index
  *         DataLength
  * @retval Crc8 value
  */
unsigned char u8GetCrc8(unsigned char *Buff,unsigned char BuffStart,unsigned char DataLength)
{
    unsigned char i;

    unsigned char volatile u8Crc8;

    u8Crc8 = 0;
    for ( i = BuffStart; i < BuffStart+DataLength; i++)
    {
        u8Crc8 ^= Buff[i];
        u8Crc8  = u8CRC8Table[u8Crc8];
    }

    return u8Crc8;
}

